package com.fmall.springmvc.javabean.savings;

/**
 * @author WHL
 *
 */
public class SavingsFetchCondition {

	private String savingsProvince = null;
	private String savingsCity = null;
	private String savingsCurrency = null;
	private String savingsType = null;
	private String savingsTerm = null;
	private String savingsBank = null;
	/**
	 * @return the savingsProvince
	 */
	public String getSavingsProvince() {
		return savingsProvince;
	}
	/**
	 * @param savingsProvince the savingsProvince to set
	 */
	public void setSavingsProvince(String savingsProvince) {
		this.savingsProvince = savingsProvince;
	}
	/**
	 * @return the savingsCity
	 */
	public String getSavingsCity() {
		return savingsCity;
	}
	/**
	 * @param savingsCity the savingsCity to set
	 */
	public void setSavingsCity(String savingsCity) {
		this.savingsCity = savingsCity;
	}
	/**
	 * @return the savingsCurrency
	 */
	public String getSavingsCurrency() {
		return savingsCurrency;
	}
	/**
	 * @param savingsCurrency the savingsCurrency to set
	 */
	public void setSavingsCurrency(String savingsCurrency) {
		this.savingsCurrency = savingsCurrency;
	}
	/**
	 * @return the savingsType
	 */
	public String getSavingsType() {
		return savingsType;
	}
	/**
	 * @param savingsType the savingsType to set
	 */
	public void setSavingsType(String savingsType) {
		this.savingsType = savingsType;
	}
	/**
	 * @return the savingsTerm
	 */
	public String getSavingsTerm() {
		return savingsTerm;
	}
	/**
	 * @param savingsTerm the savingsTerm to set
	 */
	public void setSavingsTerm(String savingsTerm) {
		this.savingsTerm = savingsTerm;
	}
	/**
	 * @return the savingsBank
	 */
	public String getSavingsBank() {
		return savingsBank;
	}
	/**
	 * @param savingsBank the savingsBank to set
	 */
	public void setSavingsBank(String savingsBank) {
		this.savingsBank = savingsBank;
	}

}
